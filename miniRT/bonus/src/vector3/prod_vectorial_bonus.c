#include "../../inc/minirt_bonus.h"

t_vec3	prod_vectorial(t_vec3 a, t_vec3 b)
{
	t_vec3	out;

	out.x = a.y * b.z - a.z * b.y;
	out.y = a.z * b.x - a.x * b.z;
	out.z = a.x * b.y - a.y * b.x;
	return (out);
}
