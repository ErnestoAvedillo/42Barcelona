#include"libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i] && s[i] != (char)c)
		i++;
	if (!s[i])
		return(NULL);
	else
		return((char*)(s + i));

}

