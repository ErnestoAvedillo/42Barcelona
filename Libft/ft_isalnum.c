int	ft_isalnum(char *str)
{
	while (*str != '\0')
	{
		if (!((*str >= 'A' && *str <= 'Z') \
			|| (*str >= 'a' && *str <= 'z') \
			|| (*str >= '0' && *str <= '9')))
		{
			return (0);
		}
		str++;
	}
	return (1);
}
